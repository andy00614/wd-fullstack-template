"use client";

import { zodResolver } from "@hookform/resolvers/zod";
import { Loader2 } from "lucide-react";
import { useState, useTransition } from "react";
import { useForm } from "react-hook-form";
import { toast } from "sonner";
import { z } from "zod";
import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { login, signInWithGoogle } from "../actions";

const loginSchema = z.object({
	email: z.email("Please enter a valid email"),
	password: z.string().min(6, "Password must be at least 6 characters"),
});

type LoginForm = z.infer<typeof loginSchema>;

export function LoginForm() {
	const [, startTransition] = useTransition();
	const [loadingMethod, setLoadingMethod] = useState<"email" | "google" | null>(
		null,
	);

	const {
		register,
		handleSubmit,
		formState: { errors },
	} = useForm<LoginForm>({
		resolver: zodResolver(loginSchema),
	});

	function onSubmit(data: LoginForm) {
		setLoadingMethod("email");
		startTransition(async () => {
			const formData = new FormData();
			formData.set("email", data.email);
			formData.set("password", data.password);
			const result = await login(formData);
			if (result?.error) {
				toast.error(result.error);
				setLoadingMethod(null);
			}
		});
	}

	function handleGoogleLogin() {
		setLoadingMethod("google");
		startTransition(async () => {
			const result = await signInWithGoogle();
			if (result?.error) {
				toast.error(result.error);
				setLoadingMethod(null);
			}
		});
	}

	return (
		<div className="flex w-full max-w-sm flex-col gap-4">
			<form className="flex flex-col gap-4" onSubmit={handleSubmit(onSubmit)}>
				<div className="space-y-2">
					<Label className="text-[#1a1a1a]" htmlFor="email">
						Email
					</Label>
					<Input
						{...register("email")}
						className="border-gray-200 bg-white text-[#1a1a1a] placeholder:text-gray-400 focus:border-[#C4B5FD] focus:ring-[#C4B5FD]"
						disabled={loadingMethod !== null}
						id="email"
						placeholder="you@example.com"
						type="email"
					/>
					{errors.email && (
						<p className="text-red-500 text-sm">{errors.email.message}</p>
					)}
				</div>
				<div className="space-y-2">
					<Label className="text-[#1a1a1a]" htmlFor="password">
						Password
					</Label>
					<Input
						{...register("password")}
						className="border-gray-200 bg-white text-[#1a1a1a] placeholder:text-gray-400 focus:border-[#C4B5FD] focus:ring-[#C4B5FD]"
						disabled={loadingMethod !== null}
						id="password"
						placeholder="Enter your password"
						type="password"
					/>
					{errors.password && (
						<p className="text-red-500 text-sm">{errors.password.message}</p>
					)}
				</div>
				<Button
					className="rounded-xl bg-[#6D28D9] font-bold text-white transition-all hover:scale-[1.02] hover:bg-[#5B21B6]"
					disabled={loadingMethod !== null}
					type="submit"
				>
					{loadingMethod === "email" && (
						<Loader2 className="mr-2 h-4 w-4 animate-spin" />
					)}
					{loadingMethod === "email" ? "Signing in..." : "Sign in"}
				</Button>
			</form>
			<div className="flex items-center gap-4">
				<div className="h-px flex-1 bg-gray-200" />
				<span className="text-gray-400 text-sm">or</span>
				<div className="h-px flex-1 bg-gray-200" />
			</div>
			<Button
				className="rounded-xl border border-gray-200 bg-white text-[#1a1a1a] shadow-sm transition-all hover:bg-gray-50 hover:shadow-md"
				disabled={loadingMethod !== null}
				onClick={handleGoogleLogin}
				type="button"
				variant="outline"
			>
				{loadingMethod === "google" ? (
					<Loader2 className="mr-2 h-4 w-4 animate-spin" />
				) : (
					<svg className="mr-2 h-4 w-4" viewBox="0 0 24 24">
						<title>Google Logo</title>
						<path
							d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
							fill="#4285F4"
						/>
						<path
							d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
							fill="#34A853"
						/>
						<path
							d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
							fill="#FBBC05"
						/>
						<path
							d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
							fill="#EA4335"
						/>
					</svg>
				)}
				Sign in with Google
			</Button>
		</div>
	);
}
